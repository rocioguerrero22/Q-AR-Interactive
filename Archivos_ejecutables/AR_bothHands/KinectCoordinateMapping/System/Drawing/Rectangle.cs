namespace System.Drawing
{
    internal class Rectangle
    {
        private object p1;
        private object p2;
        private object p3;
        private object p4;

        public Rectangle(object p1, object p2, object p3, object p4)
        {
            this.p1 = p1;
            this.p2 = p2;
            this.p3 = p3;
            this.p4 = p4;
        }
    }
}